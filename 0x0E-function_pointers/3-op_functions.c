
/**
 * op_add - adds two integers
 * @a: integer
 * @b: integer
 * Return: integer sum of a & b
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - subtracts two integers
 * @a: integer
 * @b: integer
 * Return: integer difference of a minus b
 */
int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies two integers
 * @a: integer
 * @b: integer
 * Return: integer product of a & b
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides two integers
 * @a: integer
 * @b: integer
 * Return: integer floor division of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - returns remainder of division of two integers
 * @a: integer
 * @b: integer
 * Return: integer a mod b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
